void BleSystem::init()
{
    initBleStack();
    initGapParams();
    initGatt();
    initBonding();
    _bleServices.init();
    _qwr_default_handle = _bleServices.getQwrHandle();
    advertising_init();
    initConnParameters();
}

void BleSystem::start()
{
    _bleServices.start();
    startAdvertising();
    _isActive = true;
}

void BleSystem::stop()
{
    const auto disconnect_result = sd_ble_gap_disconnect(BleSystem::getInstance().getConnectionHandle(), 
        BLE_HCI_REMOTE_USER_TERMINATED_CONNECTION);
    if (disconnect_result != 0)
    {
        NRF_LOG_WARNING("gap disconnect: err_code = 0x%x", disconnect_result);
    }
    stopAdvertising();
    _isActive = false;
}

void BleSystem::cyclic()
{
    if (_isActive)
    {
        _bleServices.cyclic();
    }
}

void BleSystem::startAdvertising()
{
    ret_code_t           err_code;

    err_code = sd_ble_gap_adv_start(m_adv_handle, APP_BLE_CONN_CFG_TAG);
    //APP_ERROR_CHECK(err_code);
    if (err_code != 0)
    {
        NRF_LOG_WARNING("startAdv: err_code = %d", err_code);
    }
}

void BleSystem::stopAdvertising()
{
    ret_code_t           err_code;

    err_code = sd_ble_gap_adv_stop(m_adv_handle);
    if (err_code != 0)
    {
        NRF_LOG_WARNING("stopAdv: err_code = %d", err_code);
    }
}

